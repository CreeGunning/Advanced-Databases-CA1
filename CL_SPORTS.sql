CREATE DATABASE CL_SPORTS;

USE CL_SPORTS;

CREATE TABLE CUSTOMERS(
CustomerID VARCHAR(6) NOT NULL,
Fname VARCHAR(20) NOT NULL,
Lname VARCHAR(20) NOT NULL,
Address VARCHAR(50) NOT NULL,
TelNo VARCHAR(10) NOT NULL,
PRIMARY KEY (CustomerID) );

CREATE TABLE ITEM_STOCK(
ItemID VARCHAR(10) NOT NULL,
Brand VARCHAR(15) NOT NULL,
Category VARCHAR(10) NOT NULL,
ItemType VARCHAR(15) NOT NULL,
Model VARCHAR(40) NOT NULL,
Price DECIMAL(5,2) NOT NULL,
Size VARCHAR(5) NOT NULL,
PRIMARY KEY (ItemID) );

CREATE TABLE PURCHASE_ORDERS(
OrderID VARCHAR(9) NOT NULL,
Delivery VARCHAR(3) NOT NULL,
PRIMARY KEY (OrderID) );

CREATE TABLE TIMELINE(
TimeID VARCHAR(6) NOT NULL,
Date_o Date NOT NULL, 
MonthID VARCHAR(2) NOT NULL,
MonthText VARCHAR(3) NOT NULL,
Year_o VARCHAR(4) NOT NULL,
PRIMARY KEY  (TimeID) );

CREATE TABLE COMPLETED_ORDER(
TimeID VARCHAR(6) NOT NULL,
CustomerID VARCHAR(6) NOT NULL,
ItemID VARCHAR(10) NOT NULL,
OrderID VARCHAR(10) NOT NULL,
TotalCost DECIMAL(5,2) NOT NULL,
PRIMARY KEY (TimeID, CustomerID, ItemID, OrderID) ,
FOREIGN KEY (TimeID) REFERENCES TIMELINE (TimeID)
ON UPDATE NO ACTION
ON DELETE NO ACTION,
FOREIGN KEY (CustomerID) REFERENCES CUSTOMERS (CustomerID)
ON UPDATE NO ACTION
ON DELETE NO ACTION,
FOREIGN KEY (ItemID) REFERENCES ITEM_STOCK (ItemID)
ON UPDATE NO ACTION
ON DELETE NO ACTION,
FOREIGN KEY (OrderID) REFERENCES PURCHASE_ORDERS (OrderID)
ON UPDATE NO ACTION
ON DELETE NO ACTION );

